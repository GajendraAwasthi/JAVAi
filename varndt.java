package JAVAi; //  the package is just folder name

public class varndt {
    public static void main(String[] args) {
        System.out.println("Hello We Are Using different datatypes Like integer, float , long , char , string 😁");

        //entering int datatype
        int var = 20;//the value of int shouldnt be in any quotes.
        System.out.println("The value of int is: "+var);
        
        //using float datatypes
        float pie = 3.25f; //the value of float shouldnt be in any quotes
        System.out.println("The value of float is: "+pie);

        //entering long datatypes
        Long numb = 98989898L;//the value of Long shouldnt be in any quotes and at back if long integer there should be "L" eg: 123456L
        System.out.println("The value of Long is: "+numb);

        //using char datatype
        char vowel = 'A';//the value of char be in single quotes
        System.out.println("The value of char is: "+vowel);

        //entering string datatype
        String text = "Hello Everyone 😁";//the value of float should be in double any quotes
        System.out.println("The value of String is: "+text);

    }
}
